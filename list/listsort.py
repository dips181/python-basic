my_list = ["apple", "banana", "cherry", "orange", "kiwi", "mango"]
my_list.sort()    #sort the list alphanumerically, ascending, by default:
print(my_list)

#Sort Descending
my_list.sort(reverse=True)
print(my_list)

#Reverse Order
my_list = ["apple", "banana", "cherry", "orange", "kiwi", "mango"]
my_list.reverse()
print(my_list)

